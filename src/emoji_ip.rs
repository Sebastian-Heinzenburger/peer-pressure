pub fn encode_ipv4(ip: Ipv4Addr) -> Vec<&'static str> {
    ip.octets()
        .iter()
        .map(|&b| EMOJIS[b as usize])
        .collect()
}

pub fn decode_ipv4(emojis: &[&str]) -> Option<Ipv4Addr> {
    if emojis.len() != 4 {
        return None;
    }

    let mut octets = [0u8; 4];

    for (i, &emoji) in emojis.iter().enumerate() {
        let index = EMOJIS.iter().position(|&e| e == emoji)?;
        octets[i] = index as u8;
    }

    Some(Ipv4Addr::from(octets))
}
